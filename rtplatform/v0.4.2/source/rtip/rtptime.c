/*----------------------------------------------------------------------*
                         rtp_get_system_msec
 *----------------------------------------------------------------------*/
unsigned long rtp_get_system_msec (void)
{
unsigned long elapsed_msec;

    elapsed_msec = ((unsigned long)ks_get_ticks( )) * ((unsigned long)ks_msec_p_tick( ));
    return (elapsed_msec);
}


/*----------------------------------------------------------------------*
                         rtp_get_system_sec
 *----------------------------------------------------------------------*/
unsigned long rtp_get_system_sec (void)
{
unsigned long elapsed_sec;

	elapsed_sec = ((unsigned long)ks_get_ticks( )) / ((unsigned long)ks_ticks_p_sec( ));
    return (elapsed_sec);
}


